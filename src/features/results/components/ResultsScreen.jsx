import { useState } from 'react'
import CreepyScore from './CreepyScore'
import BreachGrid from './BreachGrid'
import Timeline from './Timeline'
import ProfileBox from './ProfileBox'
import ActionPlan from './ActionPlan'
import ThreatIntel from './ThreatIntel'
import PasswordChecker from '../../tools/components/PasswordChecker'
import PhishingDetector from '../../tools/components/PhishingDetector'
import styles from './ResultsScreen.module.css'

const TOOLS = [
  { id: 'overview', label: 'Overview' },
  { id: 'threats', label: 'Threat Intel' },
  { id: 'password', label: 'Password Analyser' },
  { id: 'phishing', label: 'Phishing Detector' },
  { id: 'actions', label: 'Remediation' },
]

export default function ResultsScreen({ email, data, onReset }) {
  const [tab, setTab] = useState('overview')

  function handleTabKeyDown(event, index) {
    const direction = event.key === 'ArrowRight' ? 1 : event.key === 'ArrowLeft' ? -1 : 0
    if (!direction) return
    event.preventDefault()
    setTab(TOOLS[(index + direction + TOOLS.length) % TOOLS.length].id)
  }

  const profileModel = data.ai?.profile?.model || 'configured AI model'
  const threatModel = data.ai?.threatIntel?.model || 'configured AI model'

  return (
    <div className={styles.wrap}>
      <div className={styles.topBar}>
        <div className={styles.topBarLeft}>
          <span className={styles.topBarLabel}>Scan complete -</span>
          <span className={styles.topBarEmail}>{email}</span>
        </div>
        <button className={styles.resetBtn} onClick={onReset} aria-label="Start a new scan">New scan</button>
      </div>

      <CreepyScore
        score={data.creepyScore}
        label={data.scoreLabel}
        color={data.scoreColor}
        headline={data.headline}
      />

      <div className={styles.tabs} role="tablist" aria-label="Scan result sections">
        {TOOLS.map((tool, index) => (
          <button
            key={tool.id}
            role="tab"
            id={`tab-${tool.id}`}
            aria-selected={tab === tool.id}
            aria-controls={`panel-${tool.id}`}
            className={`${styles.tab} ${tab === tool.id ? styles.tabActive : ''}`}
            onClick={() => setTab(tool.id)}
            onKeyDown={event => handleTabKeyDown(event, index)}
          >
            {tool.label}
          </button>
        ))}
      </div>

      <div className={styles.tabContent}>
        {tab === 'overview' && (
          <div className={styles.section} role="tabpanel" id="panel-overview" aria-labelledby="tab-overview">
            <div className={styles.sectionHeader}>
              <span className={styles.sectionTitle}>Data breaches detected</span>
              {data.breaches.length > 0 && (
                <span className={styles.sectionCount}>{data.breaches.length} source{data.breaches.length !== 1 ? 's' : ''}</span>
              )}
            </div>
            <BreachGrid breaches={data.breaches} />

            {data.timeline?.length > 0 && (
              <>
                <div className={styles.sectionHeader} style={{ marginTop: 28 }}>
                  <span className={styles.sectionTitle}>Breach timeline</span>
                  <span className={styles.sectionCount}>your exposure history - click events to expand</span>
                </div>
                <Timeline events={data.timeline} />
              </>
            )}

            <div className={styles.divider} />

            <div className={styles.sectionHeader}>
              <span className={styles.sectionTitle}>AI-inferred profile</span>
              <span className={styles.sectionCount}>generated by {profileModel}</span>
            </div>
            <ProfileBox
              email={email}
              profile={data.profile}
              model={profileModel}
              promptVersion={data.ai?.profile?.promptVersion}
            />
          </div>
        )}

        {tab === 'threats' && (
          <div className={styles.section} role="tabpanel" id="panel-threats" aria-labelledby="tab-threats">
            <div className={styles.sectionHeader}>
              <span className={styles.sectionTitle}>Dark web threat intelligence</span>
              <span className={styles.sectionCount}>generated by {threatModel}</span>
            </div>
            <ThreatIntel data={data.threatIntel} />
          </div>
        )}

        {tab === 'password' && (
          <div className={styles.section} role="tabpanel" id="panel-password" aria-labelledby="tab-password">
            <div className={styles.sectionHeader}>
              <span className={styles.sectionTitle}>Password strength analyser</span>
              <span className={styles.sectionCount}>analysed server-side, not stored</span>
            </div>
            <div className={styles.toolNote}>
              Check if passwords associated with your breached accounts are strong enough.
              Passwords are validated by the API and are never logged.
            </div>
            <PasswordChecker />
          </div>
        )}

        {tab === 'phishing' && (
          <div className={styles.section} role="tabpanel" id="panel-phishing" aria-labelledby="tab-phishing">
            <div className={styles.sectionHeader}>
              <span className={styles.sectionTitle}>AI phishing URL detector</span>
              <span className={styles.sectionCount}>AI-assisted with deterministic fallback</span>
            </div>
            <div className={styles.toolNote}>
              Paste any suspicious link to analyse it for phishing indicators, typosquatting,
              and social engineering techniques.
            </div>
            <PhishingDetector />
          </div>
        )}

        {tab === 'actions' && (
          <div className={styles.section} role="tabpanel" id="panel-actions" aria-labelledby="tab-actions">
            <div className={styles.sectionHeader}>
              <span className={styles.sectionTitle}>Personalised remediation plan</span>
              <span className={styles.sectionCount}>{data.actions.length} priority actions</span>
            </div>
            <ActionPlan actions={data.actions} />
          </div>
        )}
      </div>

      <div className={styles.footer}>
        <button className={styles.resetBtn2} onClick={onReset}>Scan another email</button>
        <div className={styles.footerNote}>Privacy Mirror - AI + Cybersecurity - AMD Slingshot Hackathon</div>
      </div>
    </div>
  )
}
